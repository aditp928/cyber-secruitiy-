# Prints the path to the current folder
pwd

# Make or create a new directory/folder: mkdir <directory_name>
mkdir MyFolder

# Create a new, empty file with a given name: touch <filename>
touch MyFirstFile.txt

# List or show all files in the current folder
ls

# Navigate (or change directory) into the directory MyFolder
cd MyFolder

# Make or create a new folder called: MySubFolder
mkdir MySubFolder

# Navigate into MySubFolder
cd MySubFolder

# Open the current folder in a GUI in Windows
explorer .

# Open the current folder in a GUI in Mac
open .

# Clears the command line
clear 
