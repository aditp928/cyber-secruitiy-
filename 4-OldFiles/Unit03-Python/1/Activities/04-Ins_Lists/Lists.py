# Creating a list and saving it to a variable
hobbies = ["Rock Climbing", "Bug Collecting", "Cooking", "Knitting", "Writing"]
print(hobbies)

# Selecting the first value from the list
print(hobbies[0])

# Selecting the second value from the list
print(hobbies[1])

# Discovering how long a list is using len()
print(len(hobbies))

# Using index() to find the index of a specific value in a list
print(hobbies.index("Cooking"))

# Adding values onto the end of a list using append()
hobbies.append("Gaming")
print(hobbies)

# Removing a value from a list using remove()
hobbies.remove("Bug Collecting")
print(hobbies)
